def t1(a,lst1 = [1,2]):
    if a not in lst1:
        lst1.append(a)
    return lst1

print(f'第一次调用t1函数的结果是：{t1(10)}')    #[1,2,10]
print(f'第二次调用t1函数的结果是：{t1(20)}')    #不是[1,2,20],正确的是[1,2,10,20]
print(f'第三次调用t1函数的结果是：{t1(30,lst1=[60,70])}')    #改变了引用，就是改变了内存地址[60,70,30]
print(f'第四次调用t1函数的结果是：{t1(40)}')    #回到原来的内存地址[1,2,10,20,40]



